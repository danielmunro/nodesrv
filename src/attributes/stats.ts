export class Stats {
  private str: number
  private int: number
  private wis: number
  private dex: number
  private con: number
  private sta: number

  constructor(
    str,
    int,
    wis,
    dex,
    con,
    sta,
  ) {
    this.str = str
    this.int = int
    this.wis = wis
    this.dex = dex
    this.con = con
    this.sta = sta
  }
}
